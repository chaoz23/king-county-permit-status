#!/usr/bin/env python3
"""Generate the King County city × permit-type coverage scorecard.

Single source of truth: routing_data.json. Emits the markdown block that lives
in README.md between the SCORECARD markers. Re-run after any routing change:

    python3 scripts/gen_scorecard.py --check   # fail if README is stale
    python3 scripts/gen_scorecard.py --write    # rewrite the README block
"""
import json
import os
import sys

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DATA = os.path.join(ROOT, "routing_data.json")
README = os.path.join(ROOT, "README.md")

BEGIN = "<!-- BEGIN SCORECARD (generated by scripts/gen_scorecard.py) -->"
END = "<!-- END SCORECARD -->"

# Cities with a dedicated live integration (not MyBuildingPermit).
DEDICATED = {"renton": "Renton (EnerGov)",
             "seattle": "Seattle (SDCI Open Data)",
             "bellevue": "Bellevue (Open Data)"}

# Display names where title-casing the routing_data key is wrong.
NAME_OVERRIDES = {"seatac": "SeaTac"}


def display_name(city):
    key = city.lower()
    if key in NAME_OVERRIDES:
        return NAME_OVERRIDES[key]
    return " ".join(w.capitalize() for w in city.split())

CELL = {
    "yes": "✅",
    "lni": "➖ L&I",
    "gap": "⚠️ gap",
    "no": "🔴",
}


def load():
    d = json.load(open(DATA))
    kc = sorted(d["king_county_cities"], key=str.lower)
    mbp = {c.lower() for c in d["cities_on_mbp"]}
    own_elec = {c.lower() for c in d["cities_own_electrical"]}
    return d, kc, mbp, own_elec


def classify(city, mbp, own_elec):
    key = city.lower()
    on_mbp = key in mbp
    dedicated = key in DEDICATED
    owns_elec = key in own_elec
    live_building = on_mbp or dedicated

    # source label
    if dedicated:
        source = DEDICATED[key]
    elif on_mbp:
        source = "MyBuildingPermit"
    else:
        source = "—"

    # electrical — verified against MBP feeds 2026-07-15 (see B7 in the epic):
    #   MyBuildingPermit carries a city's own electrical permits, so any MBP
    #   city has full electrical history regardless of who runs the program.
    #   The real gap is a city that self-runs electrical (L&I is skipped) AND
    #   is not on MBP — its electrical lives in no feed we search.
    if dedicated or (on_mbp and owns_elec):
        electrical = "yes"          # city feed carries full electrical history
    elif on_mbp:
        electrical = "lni"          # state (L&I) issues this city's electrical, 2020+
    elif owns_elec:
        electrical = "gap"          # self-run electrical, not on MBP, L&I skipped
    else:
        electrical = "lni"          # L&I supplements (2020+)

    building = "yes" if live_building else "no"
    trade = "yes" if live_building else "no"       # mechanical / plumbing
    landuse = "yes" if live_building else "no"

    # status
    if live_building:
        status = "🟢 Live"
    elif electrical == "lni":
        status = "🟡 Partial"       # only L&I electrical retrievable; rest is a portal link
    else:
        status = "🔴 Fallback"      # link only

    return {
        "city": display_name(city), "status": status, "source": source,
        "building": building, "electrical": electrical,
        "trade": trade, "landuse": landuse,
    }


def render(rows):
    gap_cities = [r["city"] for r in rows if r["electrical"] == "gap"]
    out = [BEGIN, "", "### Coverage scorecard", "",
           "Auto-generated from `routing_data.json` "
           "(`python3 scripts/gen_scorecard.py --write`). "
           "One row per King County city.", "",
           "**Legend** — ✅ retrievable · ➖ L&I electrical only (2020+) · "
           "⚠️ gap (city self-runs electrical, not yet integrated) · 🔴 portal link only",
           "",
           "| City | Status | Source | Building | Electrical | Mech/Plumb | Land Use |",
           "|---|---|---|---|---|---|---|"]
    tally = {"🟢 Live": 0, "🟡 Partial": 0, "🔴 Fallback": 0}
    for r in rows:
        tally[r["status"]] += 1
        out.append("| {city} | {status} | {source} | {b} | {e} | {t} | {l} |".format(
            city=r["city"], status=r["status"], source=r["source"],
            b=CELL[r["building"]], e=CELL[r["electrical"]],
            t=CELL[r["trade"]], l=CELL[r["landuse"]]))
    # County-level row (unincorporated King County): building/septic/critical-areas
    # via the MyBuildingPermit "King County" jurisdiction; electrical via L&I.
    out.append("| _King County (unincorp.)_ | 🟢 Live | MyBuildingPermit (King County) "
               "| ✅ | ➖ L&I | ✅ | ✅ |")
    total = len(rows)
    out += ["",
            f"**Coverage:** {tally['🟢 Live']}/{total} live · "
            f"{tally['🟡 Partial']}/{total} partial (L&I electrical only) · "
            f"{tally['🔴 Fallback']}/{total} fallback. "
            "Plus **King County (unincorporated)** for county-level permits "
            "(septic, critical areas, grading).",
            "",
            "> ⚠️ **Electrical gaps:** " + (
                ", ".join(gap_cities) if gap_cities else "none"
            ) + ". These cities self-run their electrical program (so L&I is skipped) "
            "and are not on MyBuildingPermit, leaving their electrical permits in no "
            "feed we search. MBP cities that self-run electrical (Burien, Federal Way, "
            "Kirkland, Mercer Island, Sammamish) are **not** gaps — verified 2026-07-15 "
            "that MBP carries their electrical history. Closing the remaining gaps is "
            "tracked in the coverage epic.",
            "", END]
    return "\n".join(out)


def main():
    d, kc, mbp, own_elec = load()
    rows = [classify(c, mbp, own_elec) for c in kc]
    block = render(rows)

    mode = sys.argv[1] if len(sys.argv) > 1 else "--print"
    if mode == "--print":
        print(block)
        return
    readme = open(README).read()
    if BEGIN in readme and END in readme:
        pre = readme[:readme.index(BEGIN)]
        post = readme[readme.index(END) + len(END):]
        new = pre + block + post
    else:
        raise SystemExit("SCORECARD markers not found in README.md; add them first.")
    if mode == "--check":
        sys.exit(0 if readme == new else "README scorecard is stale — run --write")
    if mode == "--write":
        open(README, "w").write(new)
        print("README scorecard updated.")


if __name__ == "__main__":
    main()
